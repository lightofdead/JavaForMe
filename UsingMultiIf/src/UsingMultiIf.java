public class UsingMultiIf {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
       String wolf = "Волк", fox = "Лиса", mouse = "Мышь", animal;
       String choice;
       animal=JOptionPane.showInputDialog(null, wolf+", "+fox+" или "+mouse+"?",
               "Кого выберешь ты?", JOptionPane.QUESTION_MESSAGE);
       if(animal==null){
           System.exit(0);
       }
       if(animal.equalsIgnoreCase(wolf)){
           choice = Wolf.imgWolf;
       }
       else if(animal.equalsIgnoreCase(fox)){
           choice=Fox.imgFox;
       }
       else if(animal.equalsIgnoreCase(mouse)){
           choice=Mouse.mouseImg;
       }
       else{
           choice=NoNameAnimal.imgNoNameAnimal;
       }
       JOptionPane.showMessageDialog(null, "Ваш выбор: \n\n"+choice);
    }
    
}
